void	ft_ultimate_div_mod(int *a, int *b)
{
	int q;

	q = 0;
	if (*b != 0)
	{
		while (*a >= 0)
		{
			*a = *a - *b;
			q += 1;
		}
		*b = *a + *b;
		*a = q - 1;
	}
}
