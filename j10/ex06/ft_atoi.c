#include "ft.h"

int		ft_atoi(char *s)
{
	int	a;
	int	i;
	int m;

	a = 0;
	i = 0;
	m = 1;
	if (s[i] = '-')
	{
		m = -1;
		i++;
	}
	while (s[i])
	{
		a = a * 10;
		a = a + s[i] - 48;
		i++;
	}
	a = a * m;
	return (a);
}
