void	ft_div_mod(int a, int b, int *div, int *mod)
{
	if (b != 0)
	{
		*div = 0;
		while (a >= 0)
		{
			a = a - b;
			*div += 1;
		}
		*mod = a + b;
		*div = *div - 1;
	}
}
